    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        
        Funcionarios funcionarios = new Funcionarios();
    
        funcionarios.setNome(txtNomeFuncionario.getText());
        funcionarios.setCpf(txtCPF.getText());
        funcionarios.setTelefone(txtTelefone.getText());
    
        FuncionariosDAO dao = new FuncionariosDAO();
        dao.cadastrarFuncionarios(funcionarios);
        dao.cadastrarUsuario(txtLogin.getText(), txtSenha.getText());
        
        funcionarios.setCargo(txtCargo.getText());
    }//GEN-LAST:event_btnCadastrarActionPerformed

